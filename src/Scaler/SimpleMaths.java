package Scaler;

import java.util.Scanner;

public class SimpleMaths {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int A = sc.nextInt();
        int B = sc.nextInt();

        System.out.println( A + B);
        System.out.println( A - B);
        System.out.println( A * B);
        System.out.println( A / B);
    }
}
